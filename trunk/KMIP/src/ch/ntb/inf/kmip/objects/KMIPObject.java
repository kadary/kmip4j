package ch.ntb.inf.kmip.objects;

import ch.ntb.inf.kmip.kmipenum.EnumTag;



public abstract class KMIPObject {

	private EnumTag tag;
	
	public KMIPObject(EnumTag tag){
		this.tag = tag;
	}

	public EnumTag getTag(){
		return tag;
	}

}
