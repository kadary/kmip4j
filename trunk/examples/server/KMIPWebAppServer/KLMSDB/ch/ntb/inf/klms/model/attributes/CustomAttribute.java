package ch.ntb.inf.klms.model.attributes;

import javax.persistence.Entity;
import javax.persistence.Inheritance;
import javax.persistence.InheritanceType;

import ch.ntb.inf.klms.model.objects.base.Attribute;


@Entity
@Inheritance(strategy = InheritanceType.SINGLE_TABLE)
public abstract class CustomAttribute extends Attribute {

	public CustomAttribute(String attributeName, String tag, String type) {
		super(attributeName, tag, type);
	}
	
}
